function service_coaching_normalize_skill_tag(string $skillTag): string {
  $skillTag = trim($skillTag);
  if ($skillTag === '') {
    return '';
  }
  $aliases = service_coaching_skill_aliases();
  return $aliases[$skillTag] ?? $skillTag;
}

